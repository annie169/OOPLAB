import java.util.*;
class J3_OddEven
{
public static void main(String args[])
{
Scanner sc = new Scanner(System.in);

System.out.println("Enter a number : ");
int num = sc.nextInt();
if(num%2==0)

System.out.println("The entered number "+num+" is Even");

else

System.out.println("The entered number "+num+" is Odd");

}
}
